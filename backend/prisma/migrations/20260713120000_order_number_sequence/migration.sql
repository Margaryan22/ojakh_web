-- Монотонная последовательность для номеров заказов.
-- Старая схема (случайное 4-значное число с ретраями) исчерпывается после
-- ~9000 заказов. Старые номера — 1000..9999, поэтому старт с 10000
-- гарантирует отсутствие коллизий с существующими заказами.
CREATE SEQUENCE IF NOT EXISTS "order_number_seq" START WITH 10000;
